    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed

        try {
            coSoTimKiem = new ArrayList<>();
            String tenCoSoTim = txtTimKiem.getText();
            for (CoSo cs : coSos) {
                if (cs.getTenCoSo().contains(tenCoSoTim)) {
                    coSoTimKiem.add(cs);
                }
            }
            if (coSoTimKiem.isEmpty()) {
                throw new Exception("Không tìm thấy.");
            }
            model.setRowCount(0);
            for (CoSo cs : coSoTimKiem) {
                model.addRow(new Object[]{
                    cs.getMaCoSo(),
                    cs.getTenCoSo(),
                    cs.getDiaChi()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "Thông báo", JOptionPane.OK_OPTION);
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

