    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        try {
            String maCoSo = txtMaCoSo.getText();
            for (CoSo cs : coSos) {
                if (maCoSo.compareTo(cs.getMaCoSo()) == 0) {
                    throw new Exception("Mã cơ sở trùng. Mời bạn nhập lại");
                }
            }
            String tenCoSo = txtTenCoSo.getText();
            String diaChi = txtDiaChi.getText();

            CoSo coSo = new CoSo(maCoSo, tenCoSo, diaChi);
            coSos.add(coSo);
            CoSoService.insertRecord(coSo);

            model.addRow(new Object[]{
                maCoSo,
                tenCoSo,
                diaChi
            });
            
            JOptionPane.showMessageDialog(null, "Thêm cơ sở thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "Thông báo", JOptionPane.OK_OPTION);
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int kq = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc chắn muốn xoá không?", "Xác nhận xoá", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (kq == JOptionPane.YES_OPTION) {
            try {
                if (!DayNhaService.readAllDayNhaTheoCoSo(maCSchon).isEmpty()) {
                    throw new Exception("Bạn không được phép xoá cơ sở này.");
                }
                int row = tbCoSo.getSelectedRow();
                if (row == -1) {
                    throw new Exception("Bạn chưa chọn cơ sở nào. Mời bạn chọn.");
                }
                CoSoService.deleteRecord(maCSchon);
                coSos.remove(row);
                model.removeRow(row);
                
                JOptionPane.showMessageDialog(null, "Xóa cơ sở thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e.getMessage(), "Thông báo", JOptionPane.OK_OPTION);
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tbCoSoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbCoSoMouseClicked
        int row = tbCoSo.getSelectedRow();
        maCSchon = (String) tbCoSo.getValueAt(row, 0);
        String tenCSchon = (String) tbCoSo.getValueAt(row, 1);
        String diaChiCSchon = (String) tbCoSo.getValueAt(row, 2);
        txtMaCoSo.setText(maCSchon);
        txtTenCoSo.setText(tenCSchon);
        txtDiaChi.setText(diaChiCSchon);
    }//GEN-LAST:event_tbCoSoMouseClicked

