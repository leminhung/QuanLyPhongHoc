    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed

        try {
            String tenCoSoSua = txtTenCoSo.getText();
            String diaChiSua = txtDiaChi.getText();

            CoSo coSo = new CoSo(maCSchon, tenCoSoSua, diaChiSua);
            CoSoService.updateRecord(coSo);

            int row = tbCoSo.getSelectedRow();
            if (row == -1) {
                throw new Exception("Bạn chưa chọn cơ sở nào. Mời bạn chọn.");
            }
            coSos.get(row).setDiaChi(diaChiSua);
            coSos.get(row).setTenCoSo(tenCoSoSua);

            tbCoSo.setValueAt(tenCoSoSua, row, 1);
            tbCoSo.setValueAt(diaChiSua, row, 2);
            
            JOptionPane.showMessageDialog(null, "Sửa cơ sở thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "Thông báo", JOptionPane.OK_OPTION);
        }

    }//GEN-LAST:event_btnSuaActionPerformed

